<h3 style="background-color: #293d3d; width: 100%;padding: 4px; color:white;  text-align: center;border-radius: 15px;   "> Branch Details </h3>
  <p>
  Mechanical Department started with the B.E. Programme in the year 1979. It was affiliated to Karnatak University, Dharwad till 2002 & now it is  affiliated to Visvesvaraya Technological University (VTU ), Belgaum. The first batch of Mechanical Engineering  came out in 1984. Since then 28 batches have received their degrees. The department received VTU approval in 2003  for starting up of a new Research Center for catering to the growing needs of surrounding industries like foundries and hydro-pneumatic industries. The students can register here for Ph.D. and M.Sc. (Engg. by Research ) and pursue their research ambitions.</br>

</br>Foreseeing the growing needs of Post-Graduate studies, the department started three PG programs namely M.Tech. (CIM), M.Tech. (Machine Design) , M.Tech.(Industrial Engineering) in 2005,  2012 and 2016 respectively.</br></br>

</br>Over the decades, the department has established state-of-the art laboratories in various subjects which are utilized both by  undergraduate and post-graduate students for conducting experiments.</br>
<br>Vision<br>

To be recognized as a center of excellence for education, research and entrepreneurial skills in the field of Computer Science and Engineering with an aim of building creative IT professionals to meet global challenges.<br>

<br>Mission<br>

To cultivate inquisitive mindset among the students for identifying and analyzing real life problems and develop innovative computer solutions for the benefit of the society.

<br>Programme Educational Objectives (PEO) <br> 

   1. The graduates will acquire core competence in basic-science and engineering fundamentals necessary to formulate, analyze, and solve engineering problems and to pursue advanced study.<br>

2. The graduates will acquire capabilities to succeed as computer engineering professionals with an aptitude for higher education and entrepreneurship.<br>

3. The graduates will have the curiosity and desire of learning for life and self-confidence to adapt to changes.<br>

4. The graduates will maintain high professionalism and ethical standards, effective oral and written communication skills, work as part of teams on multidisciplinary projects under diverse professional environments, and relate engineering issues to the society, global economy and to emerging technologies.

 
