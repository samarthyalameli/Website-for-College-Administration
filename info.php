  <p>
The department of Information Science and Engineering being the youngest department of K.L.S, G.I.T Belagavi was started in the year 2000 with an intake of 60 students. The department houses well equipped classrooms with state of the art computing infrastructure. The department has a team of faculty members who are highly qualified and rich in experience. Their dedicated efforts and single minded pursuit of research has led to quality paper publications in national, international journals and conferences.</br>

The motto of the department is to impart valuable technical knowledge and professional ethics to mould the students towards their future. Constant mentoring of students by the academic staff periodically has facilitated in excellent learning environment .The laboratory based hands on experience of various engineering subjects inculcate a high level of confidence in the students to face emerging industry standards.</br>

To encourage talented students, the department has a formal association of I.S.E students known as Information Science Engineers Association(I.S.E.A) which conducts a series of events throughout the semester to encourage students to exhibit their talents in various activities .The series of activities culminate into a flagship event known as Technosparkz. The department is proud to be associated with Association of Computing Machinery (ACM). Being a reputed international professional body, the A.C.M provides the students a chance to contribute at national and international events.

