<h3 style="background-color: #293d3d; width: 100%;padding: 4px; color:white;  text-align: center;border-radius: 15px;   "> Branch Detailss </h3>
  <p>
The undergraduate course B.E. in Computer Science and Engineering was started in the year 1985 with a modest intake of 45 students. Over the years, the intake has increased from 45 to 120 and then 180. The post graduate course M.Tech. in Computer Science and Engineering specialization was started in the year 2006 with an intake of 18. The current intake for post graduate programme is 24. The Department has well qualified and experienced faculty and six of the 41 faculty members are Ph.D. degree holders with an average teaching experience of 15-20 years. The institute obtained autonomous status under VTU in the year 2015. The Department has both affiliated and autonomous batches in place with the current 6th semester batch under autonomous. The autonomous batch semester syllabus has been prepared with inputs from industry and academia in the Board of Studies meeting after a detailed discussion and deliberation. The primary objective of adopting a new syllabus is to make our students to be industry ready. We rigorously follow Outcome Based Education Teaching Learning paradigm to ensure improved quality teaching and learning to happen.</p>
<p>
  The research center in Computer Science and Engineering was established in the year 2006. Since its inception the Research Center has made a gradual progress with the value addition in terms of research scholars, quality research publications and in grants from VTU/VGST. The department was accredited in the year 2009 and got tier-II accreditation as per new NBA guidelines in the year 2015. Dr. Veena V. Desai is currently the Head of the Department.<br>

<br>Vision<br>

To be recognized as a center of excellence for education, research and entrepreneurial skills in the field of Computer Science and Engineering with an aim of building creative IT professionals to meet global challenges.<br>

<br>Mission<br>

To cultivate inquisitive mindset among the students for identifying and analyzing real life problems and develop innovative computer solutions for the benefit of the society.

<br>Programme Educational Objectives (PEO) <br> 

   1. The graduates will acquire core competence in basic-science and engineering fundamentals necessary to formulate, analyze, and solve engineering problems and to pursue advanced study.<br>

2. The graduates will acquire capabilities to succeed as computer engineering professionals with an aptitude for higher education and entrepreneurship.<br>

3. The graduates will have the curiosity and desire of learning for life and self-confidence to adapt to changes.<br>

4. The graduates will maintain high professionalism and ethical standards, effective oral and written communication skills, work as part of teams on multidisciplinary projects under diverse professional environments, and relate engineering issues to the society, global economy and to emerging technologies.

 
