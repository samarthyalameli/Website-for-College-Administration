<h3 style="background-color: #293d3d; width: 100%;padding: 4px; color:white;  text-align: center;border-radius: 15px;   "> Branch Details </h3>
  <p>
“Infrastructure Development”, a measure of the Nation’s Progress, is a manifestation of the oldest and core engineering discipline – CIVIL ENGINEERING. To contribute to the Nation building through participation in infrastructure projects, KLS Gogte Institute of Technology established the Department of Civil Engineering to offer under graduate programme in the year 1979 with an intake of 45, which was later enhanced to 60 in the year 2006 and further increased to 120 in 2010. Realizing that post graduate programme would add value to under graduate programme in terms of human resources, better infrastructure, laboratory and library facilities and to cater to the needs of students aspiring to pursue post graduation, M.Tech. in Structural Engineering was introduced in 1987 itself. Research environment was initiated in 2004 by establishing “Research Centre” under Visvesvaraya Technological University, Belgaum to offer Ph.D. and M.Sc. Engg. (by Research) in the Civil Engineering Sciences. Currently, the Centre offers research opportunities in the domains of soil structure interaction, earth quake engineering, bridge engineering, steel structures, geotechnical engineering, geo-environmental studies, geopolymer technology and concrete.</br>

The department is accredited twice by The National Board of Accreditation, New Delhi and is permanently affiliated to Visvesvaraya Technological University, Belgaum, Karnataka.</br>

The department is proud to have academically well qualified and professionally experienced faculty with unquestionable commitment and dedication to the noble profession coupled with high attrition in GIT. The department enjoys the services of faculty with expertise and specialization in all the major areas of civil engineering such as structural engineering, geotechnical engineering, hydraulics and water resources engineering, environmental engineering, transportation engineering, remote sensing and GIS etc.</br>

All the laboratories are well equipped with state-of-the-art testing facilities, not only to meet the university curriculum but also the industry. Concrete and survey laboratories have been modernized with some high end precision equipments under MODROB scheme of AICTE, New Delhi.</br>

The department offers innovative experimental and software based students’ projects, exposing them to the latest developments. Many student projects have been recognized and sponsored by KSCST, Bangalore and have won awards in the state level Project Exhibition and Competition – SRISTHI.</br>

The department is of the firm conviction that the all-round development of the student would ensure better professional prospects and quality life; with this conviction, the department out-stretches itself to provide value addition to students along with academics by regularly organizing technical lectures, inspirational talks, spiritual discourses, cultural events, techno-fests, workshops, seminars, paper presentation contests, meaningful celebrations of Teachers’ Day and Engineers’ Day, technical tours and field visits etc. under the aegis of Civil Engineering Association and Student Chapter of Institution of Engineers(India). Shri Balekundri Memorial Lecture-delivered by eminent field professionals is a prestigious endowment lecture being organized by the department every year for the last 13 years.</br>

The department organizes conferences, workshops, short term training programmes at the regional and national level for the benefit of faculty and working professionals. The department has been recognized as a training centre by Karnataka Engineering Research Station (KERS), Mysore to impart training to professional engineers of state government establishments in the form of Continuing Education Programmes.</br>

The department offers Design Consultancy and Quality Control & Assurance Testing Services to government organizations such as PWD, Karnataka Rural Infrastructure Development Ltd. (KRIDL) etc. and private establishments. It is also involved in the third party inspections of civil engineering works, construction auditing and verification of bills.</br>

The faculty have published 60+ technical papers in reputed International and National journals; presented papers in several International and National conferences. Some of the faculty members of the department have been taking up administrative and academic assignments such as Members of various Committees, Board of Studies, Board of Examiners  etc. of Visvesvaraya Technological University, Belgaum.</br>

Several students have brought laurels to the institute by securing University ranks. Many students graduated from the portals of Civil Engineering Department have occupied top positions in prestigious organizations across the globe; many of the students passed out from this department are entrepreneurs and professionals.</br>

The department is practicing Outcome Based Education (OBE) as per the requirements of National Board of Accreditation, New Delhi and ABET, with the following mission and vision:</br>
<br>Vision<br>

To be recognized as a center of excellence for education, research and entrepreneurial skills in the field of Computer Science and Engineering with an aim of building creative IT professionals to meet global challenges.<br>

<br>Mission<br>

To cultivate inquisitive mindset among the students for identifying and analyzing real life problems and develop innovative computer solutions for the benefit of the society.

<br>Programme Educational Objectives (PEO) <br> 

   1. The graduates will acquire core competence in basic-science and engineering fundamentals necessary to formulate, analyze, and solve engineering problems and to pursue advanced study.<br>

2. The graduates will acquire capabilities to succeed as computer engineering professionals with an aptitude for higher education and entrepreneurship.<br>

3. The graduates will have the curiosity and desire of learning for life and self-confidence to adapt to changes.<br>

4. The graduates will maintain high professionalism and ethical standards, effective oral and written communication skills, work as part of teams on multidisciplinary projects under diverse professional environments, and relate engineering issues to the society, global economy and to emerging technologies.

 
